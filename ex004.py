x = input('Digite algo: ')
print('O que foi digitado é do tipo primitivo: ',type(x))
print('{} é \033[1;31mnúmerico\033[m ? '.format(x),x.isnumeric())
print('{} é \033[1;32mdecimal\033[m ? '.format(x),x.isdecimal())
print('{} é \033[1;33mletra\033[m ? '.format(x),x.isalpha())
print('{} é \033[1;34malfanumérico\033[m ? '.format(x),x.isalnum())
print('{} é \033[1;35mmaiúsculo\033[m ? '.format(x),x.isupper())
print('{} é \033[1;36mminúsculo\033[m ? '.format(x),x.islower())
print('{} só \033[4;37mtem espaços\033[m ? '.format(x),x.isspace())
print('{} é \033[7;37mcapitalizada\033[m ? '.format(x),x.istitle())